// Q.1 Write a Program to find the area of a circle

#include<stdio.h>
int main()
{
    float pi=3.14,r;
    
    printf("\n________________________________________________________________\n");
    
    printf("Enter the radius :- ");
    scanf("%f",&r);
    
    printf("\n________________________________________________________________\n");
    
    
    printf("\n Area of circle is :- %.2f",pi*r*r);
    printf("\n________________________________________________________________\n");
    
    return 0;
}