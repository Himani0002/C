#include<stdio.h>
int main()
{
    float p,r,n;
    printf("Enter the value of p :- ");
    scanf("%f",&p);
    printf("Enter the value of r :- ");
    scanf("%f",&r);
    printf("Enter the value of n :- ");
    scanf("%f",&n);
    
    printf("Simple interest :- .2%f",p*r*n);
    return 0;
    
}